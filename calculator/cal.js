export const cal = {
  plus: function (a, b) {
    const result = a + b;
    return result;
  },
  minus: function (a, b) {
    const result = a - b;
    return result;
  },
  times: function (a, b) {
    const result = a * b;
    return result;
  },
  div: function (a, b) {
    const result = a / b;
    return result;
  },
};
